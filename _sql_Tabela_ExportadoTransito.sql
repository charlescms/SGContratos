Select
  Fatura.Contrato AS Contrato,
  Fatura.Seq AS Seq,
  Fatura.Status AS Status,
  ContratoTransporte.StatusCT AS StatusCT,
  ContratoTransporte.NomeMix AS NomeMix,
  Fatura.Navio AS Navio,
  Fatura.DataEmbarque AS DataEmbarque,
  Fatura.QuantidadeExp AS QuantidadeExp,
  Fatura.Fatura AS Fatura,
  Fatura.StDocumento AS StDocumento,
  ContratoTransporte.FormComissao AS FormComissao,
  ContratoTransporte.Comissao AS Comissao,
  Fatura.Valor AS Valor,
  Fatura.ValorDesconto AS ValorDesconto,
  Fatura.Cadastro AS Cadastro,
  Fatura.ETADestino AS ETADestino,
  Fatura.FormPago AS FormPago,
  Fatura.PrecoFOB AS PrecoFOB,
  Fatura.ComisaoSFatura AS ComisaoSFatura,
  Fatura.DataOrdCobranca AS DataOrdCobranca,
  Fatura.Rate AS Rate,
  Fatura.ValorConvertido AS ValorConvertido,
  Fatura.NumNFEmit AS NumNFEmit,
  Fatura.NumOrdCobranca AS NumOrdCobranca,
  Fatura.DataNF AS DataNF,
  Fatura.OBS AS OBS,
  Fatura.Comentario AS Comentario,
  Fatura.DataLiq AS DataLiq,
  ContratoTransporte.FORCOD AS FORCOD,
  Fornecedores.MOEDACOMISSAO AS MOEDACOMISSAO,
  Fornecedores.RAZAO AS RAZAO,
  Fatura.Recebimento AS Recebimento,
  Fatura.Valor_NF AS Valor_NF,
  Fatura.Selecionado AS Selecionado,
  Fatura.MOEDACOMISSAOTAXA AS MOEDACOMISSAOTAXA
from ( select      
       ExportadoTransito.Contrato AS Contrato,
       ExportadoTransito.Seq AS Seq,
       ExportadoTransito.Status AS Status,
       ExportadoTransito.Navio AS Navio,
       ExportadoTransito.DataEmbarque AS DataEmbarque,
       ExportadoTransito.QuantidadeExp AS QuantidadeExp,
       ExportadoTransito.Fatura AS Fatura,
       ExportadoTransito.StDocumento AS StDocumento,
       ExportadoTransito.Valor AS Valor,
       ExportadoTransito.ValorDesconto AS ValorDesconto,
       ExportadoTransito.Cadastro AS Cadastro,
       ExportadoTransito.ETADestino AS ETADestino,
       ExportadoTransito.FormPago AS FormPago,
       ExportadoTransito.PrecoFOB AS PrecoFOB,
       ExportadoTransito.ComisaoSFatura AS ComisaoSFatura,
       ExportadoTransito.DataOrdCobranca AS DataOrdCobranca,
       ExportadoTransito.Rate AS Rate,
       ExportadoTransito.ValorConvertido AS ValorConvertido,
       ExportadoTransito.NumNFEmit AS NumNFEmit,
       ExportadoTransito.NumOrdCobranca AS NumOrdCobranca,
       ExportadoTransito.DataNF AS DataNF,
       ExportadoTransito.OBS AS OBS,
       ExportadoTransito.Comentario AS Comentario,
       ExportadoTransito.DataLiq AS DataLiq,
       ExportadoTransito.Recebimento AS Recebimento,
       ExportadoTransito.Valor_NF AS Valor_NF,
       ExportadoTransito.Selecionado AS Selecionado,
       ExportadoTransito.MOEDACOMISSAOTAXA AS MOEDACOMISSAOTAXA
       from  ExportadoTransito
     ) as  Fatura
Left Outer Join ContratoTransporte on
  Fatura.Contrato = ContratoTransporte.Contrato
Left Outer Join Fornecedores on
  ContratoTransporte.FORCOD = Fornecedores.FORCOD
Order By Fatura.Cadastro DESC
